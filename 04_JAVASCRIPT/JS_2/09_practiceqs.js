//Create a Traffic Light system that shows what to do based on color
// Red -> Stop 
// Yellow -> Slow Down 
// Green -> Go 

let color = "Green";

//Traffic Light System
if (color=="Red"){
    console.log("Stop !");
}
if(color=="Yellow"){
    console.log("Slow Down");
}
if(color =="Green"){
    console.log("Green");
}